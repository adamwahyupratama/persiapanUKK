    private void tampilDataPengaduan(){
    DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID Pengaduan");
        model.addColumn("TGl Pengaduan");
        model.addColumn("NIK");
        model.addColumn("ISI Laporan");
        jTable2.setModel(model);
        try {
            Statement state = konek.createStatement();
            ResultSet data = state.executeQuery("SELECT * FROM pengaduan");
            while (data.next()) {
                model.addRow(new Object[]{
                    data.getString("id_pengaduan"),
                    data.getString("tgl_pengaduan"),
                    data.getString("nik"),
                    data.getString("isi_laporan"),});
                jTable2.setModel(model);
            }
        } catch (Exception e) {
            System.err.println("Terjadi Kesalahan" + e);
        }
    }
    private void tampilDataTanggapan(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID Tanggapan");      
        model.addColumn("ID Pengaduan");
        model.addColumn("Tanggal");
        model.addColumn("Tanggapan");
        model.addColumn("ID Petugas");
        jTable1.setModel(model);
        try {
            Statement state = konek.createStatement();
            ResultSet data = state.executeQuery("SELECT * FROM tanggapan");
            while (data.next()) {
                model.addRow(new Object[]{
                    data.getString("id_tanggapan"),
                    data.getString("id_pengaduan"),
                    data.getString("tgl_tanggapan"),
                    data.getString("tanggapan"),
                    data.getString("id_petugas")
                });
                jTable1.setModel(model);
            }
        } catch (Exception e) {
            System.err.println("Terjadi Kesalahan" + e);
        }
    }

