    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int ok = JOptionPane.showConfirmDialog(null, "Apakah yakin ingin Update data ini?", "Konfirmasi Dialog", JOptionPane.YES_NO_OPTION);
        if (ok == 0) {
            try {
                SimpleDateFormat fm = new SimpleDateFormat("yyyy-MM-dd");
                String tgltanggap = String.valueOf(fm.format(jDateChooser1.getDate()));
                String sql = "update tanggapan set id_pengaduan ='" + jTextField5.getText()
                        + "', tgl_tanggapan ='" + tgltanggap
                        + "', tanggapan ='" + jTextArea2.getText()
                        + "', id_petugas ='" + jTextField2.getText()
                        + "' where id_tanggapan='" + jTextField1.getText().trim() + "'";
                Statement stat = konek.createStatement();
                stat.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Data Berhasil dirubah!!!", "Ubah Data", JOptionPane.INFORMATION_MESSAGE);
                stat.close();
                jTextField1.setText("");
                jTextField2.setText("");
                jTextArea2.setText("");
                jTextField5.setText("");
                jTextField2.enable(true);
                jTextField2.requestFocus();
                tampilDataTanggapan();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Perubahan data gagal" + e.getMessage());
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

