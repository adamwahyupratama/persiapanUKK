    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
       String search = jTextField3.getText();
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID Tanggapan");
        model.addColumn("ID Pengaduan");
        model.addColumn("Tanggal");
        model.addColumn("Tanggapan");
        model.addColumn("ID Petugas");
        jTable2.setModel(model);
        try {
            Statement cari = konek.createStatement();
            ResultSet data = cari.executeQuery("select * from tanggapan where id_tanggapan like '%"
                    + search + "%' or id_pengaduan like '%" + search + "%' or tgl_tanggapan like '%"
                    + search + "%' or tanggapan like '%" + search + "%' or id_petugas like '%" + search + "%'");
            while (data.next()) {
                model.addRow(new Object[]{
                    data.getString("id_tanggapan"),
                    data.getString("id_pengaduan"),
                    data.getString("tgl_tanggapan"),
                    data.getString("tanggapan"),
                    data.getString("id_petugas")
                });
                jTable1.setModel(model);
            }
            jTextField3.setText("");
        } catch (Exception e) {
            System.err.println("Terjadi kesalahan" + e);
        }

    }//GEN-LAST:event_jButton6ActionPerformed

